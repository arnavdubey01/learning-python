#Union and intersection of sets.

s1 = {1, 4, 66, 52, 2}
s2 = {3, 67, 84, 72, 2}

print(s1.union(s2))         #Union of both sets s1 and s2, i.e. both combined (No repitition of elements).

print(s1.intersection(s2))  #Intersection of both sets, i.e. Elements common in s1 and s2 only.


