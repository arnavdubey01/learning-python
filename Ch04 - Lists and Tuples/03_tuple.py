a = (1, 2, 5, 6)
print(type(a)) #This tells us that type of 'a' here is tuple.

b = (2)
print(type(b)) #This prints type of 'b' as integer. But we want tuple.
#To get a single element tuple, we use comma ','; see below:
b = (2,)
print(type(b)) #Now python tells us that the type of 'b' is tuple.

#Usually we deal with tuble with multiple elements.

