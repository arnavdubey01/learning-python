for i in range(100):
    if (i==34):
        break   #break command breaks the loop (exits loop)
    print(i)

    
for i in range(100):
    if (i==34):
        continue    #Skips iteration (34 excluded/skipped)
    print(i)    

#break command kills the loop, such that nothing follows after that point, 
#while continue comomand skips that point, and continues the rest.

    