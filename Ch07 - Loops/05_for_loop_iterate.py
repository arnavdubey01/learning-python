#In Python, iteration is the process of repeatedly accessing elements from a sequence or collection one at a time, 
#or executing a block of code multiple times. 
#It enables you to automate repetitive tasks and traverse data structures seamlessly.


l = [1, 4, 6, 234, 6, 764]  #list
for i in l:
    print(i)


t = (6, 231, 75, 122)   #tuple
for i in t:
    print(i)


s = "Arnav" #string
for i in s:
    print(i)