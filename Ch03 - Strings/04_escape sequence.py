a = "He is a good boy\nbut not a bad\n boy"
print(a)
#Here, "\n" is a escape sequence character used to get output in next line.


#Another escape sequence character is \t, used to give more space between words.
b = "He is a good\t boy\nbut not a bad boy"
print(b)


#Now, suppose you want to add a double quote inside the string, within the double quotes of string, it will cause confusion.
#So we have another escape sequence character to fix it, we write it as  \"word\"
c = "He is a good boy\nBut not a \"bad\" boy"
print(c)


# \; - single quote
# \\ - back slash